#Set : គឺជា collect of data ដែល unordered, unchangeable, and unindexed.
# មិនត្រូវ អនុញ្ញាតឱ្យមាន duplicate value និងមិនអាចផ្លាស់តម្លៃបានក្រោយពេលបង្កើត។

# Set ត្រូវបានប្រើសម្រាប់ store unique items និងធ្វើការប្រតិបត្តិលើទិន្នន័យដែលមិនចាំបាច់ត្រូវបានរៀបចំឡើងវិញ។
# Set ត្រូវបានបង្កើតដោយប្រើ curly braces {} ឬក៏ function set()។
# Example of set
# The set data type in Python is mutable, which means you can add and remove items after the set is created.
my_set = {1, 2, 3, 4, 5}
print("Set:", my_set)
# Accessing items in a set (Note: Sets are unordered, so we cannot access items by index)

# Adding items to a set
my_set.add(6)
print("Set after adding 6:", my_set)
# Removing items from a set
my_set.remove(3)
print("Set after removing 3:", my_set)




